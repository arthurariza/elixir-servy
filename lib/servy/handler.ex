defmodule Servy.Handler do
  @moduledoc "Module for hadling responses"

  import Servy.Parser
  import Servy.Plugins
  import Servy.FileHandler

  @doc "Handles a request and sends a reponse"
  def handle(request) do
    request
    |> parse
    |> rewrite_path
    |> log
    |> route
    |> track
    |> format_response
  end
end

